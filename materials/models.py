from django.db import models

class Material(models.Model):
    label_note = models.CharField('ラベル用備考', max_length=255)
    label_issue_count = models.CharField('ラベル発行枚数', max_length=100)
    retest_extension_expiry = models.CharField('リテスト延長使用期限', max_length=100)
    retest_days = models.CharField('リテスト試験日数', max_length=100)
    usage_form = models.CharField('使用剤形', max_length=100)
    expiry_display = models.CharField('使用期限表示', max_length=100)
    guarantee_period = models.CharField('保障期間', max_length=100)
    tolerance_usage = models.CharField('公差使用', max_length=100)
    category = models.CharField('分類', max_length=100)
    unit_price = models.CharField('単価', max_length=100)
    material_id = models.CharField('原料ID', max_length=100, unique=True)
    material_code = models.CharField('原料コード', max_length=100)
    material_category = models.CharField('原料区分', max_length=100)
    material_name = models.CharField('原料名', max_length=100)
    material_sub_code = models.CharField('原料簿コード（サブ）', max_length=100)
    material_main_code = models.CharField('原料簿コード（メイン）', max_length=100)
    origin_country = models.CharField('原産国表示', max_length=100)
    post_test_expiry = models.CharField('受入試験後使用期限', max_length=100)
    qc_note = models.CharField('品質管理備考', max_length=255)
    product_name = models.CharField('商品名', max_length=100)
    product_kana = models.CharField('商品名カナ', max_length=100)
    stock_unit_coefficient = models.CharField('在庫単位（係数）', max_length=100)
    change_request = models.CharField('変更申請／変更指示', max_length=100)
    diff_warn_rate = models.CharField('差分警告割合', max_length=100)
    main_bag_weighing = models.CharField('正袋秤量', max_length=100)
    main_bag_weight = models.CharField('正袋重量', max_length=100)
    hq_note = models.CharField('生産本部備考', max_length=255)
    image_path = models.CharField('画像パス', max_length=255)
    order_unit = models.CharField('発注単位', max_length=100)
    packaging = models.CharField('荷姿', max_length=100)
    correction_info = models.CharField('補正情報', max_length=100)
    manufacturer = models.CharField('製造所', max_length=100)
    standard = models.CharField('規格', max_length=100)
    procurement_type = models.CharField('調達区分', max_length=100)
    supplier = models.CharField('販売者', max_length=100)
    tare_weight = models.CharField('風袋重量', max_length=100)
    unnamed_36 = models.CharField('Unnamed: 36', max_length=100)
    unnamed_37 = models.CharField('Unnamed: 37', max_length=100)

    is_active = models.BooleanField('有効', default=True)
    created_at = models.DateTimeField('作成日時', auto_now_add=True)
    updated_at = models.DateTimeField('更新日時', auto_now=True)

    def __str__(self):
        return self.material_name or self.material_id
